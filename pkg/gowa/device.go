// IsDeviceConnected checks if a specific device is connected and logged in.
func (c *Client) IsDeviceConnected(ctx context.Context, deviceID string) (bool, error) {
	status, err := c.GetDeviceStatus(ctx, deviceID)
	if err != nil {
		return false, err
	}
	return status.IsConnected && status.IsLoggedIn, nil
}
