package gowa

import (
	"context"
	"fmt"
	"net/http"
)

// ListDevices returns all registered devices with their connection status.
func (c *Client) ListDevices(ctx context.Context) ([]DeviceInfo, error) {
	body, statusCode, err := c.doRequest(ctx, http.MethodGet, "/devices", nil, "")
	if err != nil {
		return nil, fmt.Errorf("list devices request failed: %w", err)
	}

	var result DeviceListResponse
	if err := parseResponse(body, statusCode, &result); err != nil {
		return nil, fmt.Errorf("list devices failed: %w", err)
	}

	return result.Results, nil
}

// AddDevice creates a new device slot for multi-device management.
// If deviceID is empty, one will be generated by the server.
func (c *Client) AddDevice(ctx context.Context, deviceID string) (*DeviceInfo, error) {
	req := AddDeviceRequest{}
	if deviceID != "" {
		req.DeviceID = deviceID
	}

	body, statusCode, err := c.doRequest(ctx, http.MethodPost, "/devices", req, "")
	if err != nil {
		return nil, fmt.Errorf("add device request failed: %w", err)
	}

	var result DeviceAddResponse
	if err := parseResponse(body, statusCode, &result); err != nil {
		return nil, fmt.Errorf("add device failed: %w", err)
	}

	return &result.Results, nil
}

