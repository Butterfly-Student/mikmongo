// ─────────────────────────────────────────────────────────────────────────────
// main
// ─────────────────────────────────────────────────────────────────────────────

func main() {
	ctx, cancel := signal.NotifyContext(context.Background(), os.Interrupt, syscall.SIGTERM)
	defer cancel()

	// ── Redis ──────────────────────────────────────────────────────────────────
	rdb := redis.NewClient(&redis.Options{Addr: redisAddr})
	if err := rdb.Ping(ctx).Err(); err != nil {
		log.Fatalf("redis connect (%s): %v", redisAddr, err)
	}
	log.Printf("✓ Redis        %s", redisAddr)

	// ── InfluxDB v3 ────────────────────────────────────────────────────────────
	influx, err := influxdb3.New(influxdb3.ClientConfig{
		Host:     influxURL,
		Token:    influxToken(),
		Database: influxDatabase,
	})
	if err != nil {
		log.Fatalf("influxdb3 client: %v", err)
	}
	defer influx.Close()
	log.Printf("✓ InfluxDB v3  %s  database=%s", influxURL, influxDatabase)

	// ── MikroTik ───────────────────────────────────────────────────────────────
	cfg := client.Config{
		Host:     routerHost,
		Port:     routerPort,
		Username: routerUser,
		Password: routerPass,
		Timeout:  10 * time.Second,
	}
	mikClient, err := client.New(cfg)
	if err != nil {
		log.Fatalf("mikrotik connect (%s:%d): %v", routerHost, routerPort, err)
	}
	defer mikClient.Close()
	log.Printf("✓ MikroTik     %s:%d  user=%s", routerHost, routerPort, routerUser)

	// ── Streaming ──────────────────────────────────────────────────────────────
	go runStream(ctx, mikClient, rdb, influx)

	// ── HTTP server ────────────────────────────────────────────────────────────
	mux := http.NewServeMux()
	mux.HandleFunc("/api/live", sseHandler(rdb))
	mux.HandleFunc("/api/history", historyHandler(influx))
	mux.HandleFunc("/api/interfaces", listHandler(mikClient, "/interface/print", "=.proplist=name,type,running,disabled"))
	mux.HandleFunc("/api/queues", listHandler(mikClient, "/queue/simple/print", "=.proplist=name,target"))
	mux.Handle("/", staticHandler())

	srv := &http.Server{Addr: httpAddr, Handler: mux}
	go func() {
		log.Printf("✓ Dashboard    http://localhost%s", httpAddr)
		if err := srv.ListenAndServe(); err != nil && err != http.ErrServerClosed {
			log.Fatalf("http: %v", err)
		}
	}()

	<-ctx.Done()
	log.Println("Shutting down…")
	shutCtx, sc := context.WithTimeout(context.Background(), 5*time.Second)
	defer sc()
	_ = srv.Shutdown(shutCtx)
	log.Println("Done.")
}

// ─────────────────────────────────────────────────────────────────────────────
// Streaming loop
// ─────────────────────────────────────────────────────────────────────────────

func runStream(
	ctx context.Context,
	mikClient *client.Client,
	rdb *redis.Client,
	influx *influxdb3.Client,
) {
	commands := monitorCommands()
	args := make([][]string, len(commands))
	for i, c := range commands {
		args[i] = c.args
	}

	delay := 2 * time.Second
	for {
		if ctx.Err() != nil {
			return
		}

		ch, err := mikClient.ListenManyArgsContext(ctx, args, 256)
		if err != nil {
			log.Printf("stream start error: %v — retry in %v", err, delay)
			select {
			case <-ctx.Done():
				return
			case <-time.After(delay):
			}
			if delay < 30*time.Second {
				delay *= 2
			}
			continue
		}
		delay = 2 * time.Second
		log.Printf("Streaming %d commands…", len(commands))

		for ev := range ch {
			if ev.Err != nil {
				log.Printf("event error [%d]: %v", ev.Index, ev.Err)
				continue
			}
			cmd := commands[ev.Index]
			now := time.Now()

			// Publish to Redis → SSE
			payload, _ := json.Marshal(liveEvent{
				Timestamp: now.Format(time.RFC3339Nano),
				Topic:     cmd.topic,
				Fields:    ev.Map,
			})
			rdb.Publish(ctx, redisChan, payload)

			// Cache latest in Redis HSET (60s TTL)
			key := "monitor:cache:" + cmd.topic
			hf := make(map[string]interface{}, len(ev.Map)+1)
			for k, v := range ev.Map {
				hf[k] = v
			}
			hf["_ts"] = now.Unix()
			rdb.HSet(ctx, key, hf)
			rdb.Expire(ctx, key, 60*time.Second)

			// Write numeric fields to InfluxDB v3
			p := parse.ForTopic(cmd.topic)
			tags, fields := p.Parse(ev.Map)
			if len(fields) > 0 {
				pt := influxdb3.NewPointWithMeasurement(cmd.topic)
				pt.SetTimestamp(now)
				pt.SetTag("host", routerHost)
				for k, v := range tags {
					pt.SetTag(k, v)
				}
				for k, v := range fields {
					pt.SetField(k, v)
				}
				if err := influx.WritePoints(ctx, []*influxdb3.Point{pt}); err != nil {
					log.Printf("influx write [%s]: %v", cmd.topic, err)
				}
			}
		}

		if ctx.Err() != nil {
			return
		}
		log.Printf("Stream closed, reconnecting in %v…", delay)
		select {
		case <-ctx.Done():
			return
		case <-time.After(delay):
		}
	}
}

