// Package main — standalone MikroTik monitoring dashboard (InfluxDB v3).
//
// Connects to MikroTik, streams monitoring data to Redis Pub/Sub (live SSE)
// and InfluxDB v3 (historical SQL queries). Serves dashboard at :9090.
//
// First-time setup:
//
//	docker compose -f deployments/docker-compose.monitor.yml up -d
//	docker exec influxdb3-core influxdb3 create token --admin  # save the token
//	docker exec influxdb3-core influxdb3 create database mikrotik
//
// Then set INFLUX_TOKEN env var and run:
//
//	INFLUX_TOKEN=<token> go run ./cmd/mikrotik
//	# or just update the influxToken const below
