// sseHandler subscribes to Redis Pub/Sub → streams to browser via SSE.
func sseHandler(rdb *redis.Client) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		fl, ok := w.(http.Flusher)
		if !ok {
			http.Error(w, "streaming not supported", 500)
			return
		}
		w.Header().Set("Content-Type", "text/event-stream")
		w.Header().Set("Cache-Control", "no-cache")
		w.Header().Set("Connection", "keep-alive")
		w.Header().Set("Access-Control-Allow-Origin", "*")

		sub := rdb.Subscribe(r.Context(), redisChan)
		defer sub.Close()

		for {
			select {
			case <-r.Context().Done():
				return
			case msg, ok := <-sub.Channel():
				if !ok {
					return
				}
				fmt.Fprintf(w, "data: %s\n\n", msg.Payload)
				fl.Flush()
			}
		}
	}
}

// historyHandler queries InfluxDB v3 via SQL and returns JSON.
//
// Query params:
//
//	topic    — measurement name (default: system-resource)
//	duration — lookback window  (default: 15m)  e.g. 1h, 6h, 24h
//	name     — optional tag filter for interface or queue name
func historyHandler(influx *influxdb3.Client) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Access-Control-Allow-Origin", "*")
		w.Header().Set("Content-Type", "application/json")

		topic := r.URL.Query().Get("topic")
		if topic == "" {
			topic = "system-resource"
		}
		duration := r.URL.Query().Get("duration")
		if duration == "" {
			duration = "15m"
		}
		nameFilter := r.URL.Query().Get("name")

		// Sanitise inputs (only allow alphanumeric, dash, underscore)
		topic = sanitise(topic)
		duration = sanitise(duration)
		nameFilter = sanitise(nameFilter)

		// Build SQL WHERE clause
		whereClause := fmt.Sprintf(`WHERE time >= now() - interval '%s'`, durationToInterval(duration))
		if nameFilter != "" {
			whereClause += fmt.Sprintf(` AND "name" = '%s'`, nameFilter)
		}

		sql := fmt.Sprintf(
			`SELECT * FROM "%s" %s ORDER BY time`,
			topic, whereClause,
		)

		iter, err := influx.Query(r.Context(), sql)
		if err != nil {
			http.Error(w, fmt.Sprintf(`{"error":%q}`, err.Error()), 500)
			return
		}
		defer iter.Done()

		type row = map[string]interface{}
		var rows []row

		for iter.Next() {
			rec := iter.Value()
			r2 := make(row, len(rec))
			for k, v := range rec {
				// Convert time column to RFC3339 string
				if k == "time" {
					if t, ok := v.(time.Time); ok {
						r2["time"] = t.Format(time.RFC3339)
						continue
					}
				}
				// Skip internal InfluxDB columns
				if strings.HasPrefix(k, "iox::") {
					continue
				}
				r2[k] = v
			}
			rows = append(rows, r2)
		}
		if err := iter.Err(); err != nil {
			http.Error(w, fmt.Sprintf(`{"error":%q}`, err.Error()), 500)
			return
		}

		if rows == nil {
			rows = []row{}
		}
		json.NewEncoder(w).Encode(rows)
	}
}

// listHandler runs a one-shot RouterOS command and returns result as JSON.
func listHandler(mikClient *client.Client, path string, args ...string) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Access-Control-Allow-Origin", "*")
		w.Header().Set("Content-Type", "application/json")
		cmdArgs := append([]string{path}, args...)
		reply, err := mikClient.RunArgsContext(r.Context(), cmdArgs)
		if err != nil {
			http.Error(w, fmt.Sprintf(`{"error":%q}`, err.Error()), 500)
			return
		}
		rows := make([]map[string]string, 0, len(reply.Re))
		for _, re := range reply.Re {
			rows = append(rows, re.Map)
		}
		json.NewEncoder(w).Encode(rows)
	}
}

// staticHandler serves the embedded static/ directory.
func staticHandler() http.Handler {
	sub, err := fs.Sub(staticFiles, "static")
	if err != nil {
		log.Fatalf("embed static sub: %v", err)
	}
	return http.FileServer(http.FS(sub))
}

