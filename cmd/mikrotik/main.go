// durationToInterval converts "15m", "1h", "6h", "24h" → SQL interval string.
func durationToInterval(d string) string {
	switch d {
	case "5m":
		return "5 minutes"
	case "10m":
		return "10 minutes"
	case "15m":
		return "15 minutes"
	case "30m":
		return "30 minutes"
	case "1h":
		return "1 hour"
	case "6h":
		return "6 hours"
	case "24h":
		return "24 hours"
	default:
		return "15 minutes"
	}
}

// sanitise keeps only alphanumeric characters, dash, underscore, and dot.
// Used to prevent SQL injection in query parameters.
func sanitise(s string) string {
	var b strings.Builder
	for _, r := range s {
		if (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') ||
			(r >= '0' && r <= '9') || r == '-' || r == '_' || r == '.' {
			b.WriteRune(r)
		}
	}
	return b.String()
}

